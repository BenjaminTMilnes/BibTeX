namespace BibTeX
{
    public enum BibTeXMonthStyle
    {
        /// <summary>
        /// Full month names, e.g. January, February, March ...
        /// </summary>
        January = 0,

        /// <summary>
        /// Abbreviated month names, e.g. Jan, Feb, Mar ...
        /// </summary>
        Jan = 1,

        /// <summary>
        /// Numbers, e.g. 1, 2, 3 ... starting at 1.
        /// </summary>
        Numeric = 2
    }
}
